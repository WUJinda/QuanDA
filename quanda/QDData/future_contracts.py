def get_all_contracts():
    """获取所有合约代码"""
    contracts = []
    for product_contracts in FUTURE_CONTRACTS_2025.values():
        contracts.extend(product_contracts)
    return sorted(contracts)


def get_contracts_by_product(product_code: str):
    """根据品种代码获取合约列表"""
    return FUTURE_CONTRACTS_2025.get(product_code.upper(), [])


def get_main_contracts():
    """获取主力合约（每个品种的第一个合约）"""
    main_contracts = []
    for product, contracts in FUTURE_CONTRACTS_2025.items():
        if contracts:
            main_contracts.append(contracts[0])
    return main_contracts
